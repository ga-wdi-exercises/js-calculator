var calculator = {
  add: function(num1, num2) {
    return (num1 + num2);
  },
  subtraction: function(num1, num2) {
    return (num1 - num2);
  },
  multiplication: function(num1, num2) {
    return (num1 * num2);
  },
  division: function(num1, num2) {
    return (num1 / num2);
  },
  exponents: function (num1, num2) {
    return Math.pow(num1, num2);
  }
}
