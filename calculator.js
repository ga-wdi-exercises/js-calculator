
var calculator={

  add:function(num1,num2){
    return num1 + num2;
  },
  sub:function(num1,num2){
    return num1 - num2;
  },
  mul:function(num1,num2){
    return num1 * num2;
  },
  div:function(num1,num2){
    return num1 / num2;
  },
  exp:function(num1,num2){
    return Math.pow(num1,num2);
  },
  rem:function(num1,num2){
    return num1 % num2;
  }
}
